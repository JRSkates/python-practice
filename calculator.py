class Calculator:
  def add(self, x, y):
    return x + y
  
  def subtract(self, x, y):
    return x - y
  
  def multiply(self, x, y):
    return x * y
  
  def divide(self, x, y):
    return x / y

  def raise_to_power_of(self, x, y):
    return x**y
  
  def remainder(self, x, y):
    return x % y